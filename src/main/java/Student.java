
public class Student {
	int rollNumber;
	String name;
	String subject1;
	String subject2;
	String subject3;
	
	public Student() {	}

	public Student(int rollNumber, String name, String subject1, String subject2, String subject3) {
		super();
		this.rollNumber = rollNumber;
		this.name = name;
		this.subject1 = subject1;
		this.subject2 = subject2;
		this.subject3 = subject3;
	}
	
	
}
